//연산자의 우선순위와 결합방향
// 대입 연산자

public class ArithOp {

	public static void main(String[] args) {
		int num1 = 7;
		int num2 = 3;
		
		System.out.println("num1 + num2 =" + (num1 + num2));
		System.out.println("num1 - num2 =" + (num1 - num2));
		System.out.println("num1 * num2 =" + (num1 * num2));
		System.out.println("num1 / num2 =" + (num1 / num2));
		System.out.println("num1 % num2 =" + (num1 % num2));

	}

}
